fun main() {
    var counter = 8
    do{
        println("nilai $counter")
        counter--
    }while (counter > 0)
}
/*
    Do While bersifat Exit Controlled Loop di mana proses perulangan akan langsung dijalankan di awal.
    Jika telah selesai, barulah kondisi yang diberikan akan dievaluasi.
 */