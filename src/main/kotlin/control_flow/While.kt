fun main() {
    //contoh while
    var nilai: Int = 8
    while(nilai > 0){
        println("nilai $nilai")
        nilai--
    }
}
/*
    While bersifat Entry Controlled Loop. Artinya, kondisi yang diberikan akan dievaluasi terlebih dahulu.
    Jika kondisi tersebut terpenuhi maka proses perulangan akan dijalankan.
 */